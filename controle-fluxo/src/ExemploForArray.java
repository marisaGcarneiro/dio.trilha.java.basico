public class ExemploForArray {
    public static void main(String[] args) {
        String alunos[] = { "FELIPE", "JONAS", "JULIA", "MARCIO" }; //elementos de uma array

        for (int x=0; x < alunos.length; x++) { //estrutura de blocos de validação

            System.out.println("O aluno no indice x=" + x + " é " + alunos [ x ] );

        }
    }

}
